public class ChartoASCII
{
    public static void main(String args[])
    {
        char chA='A',chZ='Z',cha='a',chz='z';
        int ascA,ascZ,asca,ascz;
        ascA=chA;
        ascZ=chZ;
        asca=cha;
        ascz=chz;
        System.out.println(ascA + "\n" + ascZ + "\n" + asca + "\n" + ascz);
    }
}